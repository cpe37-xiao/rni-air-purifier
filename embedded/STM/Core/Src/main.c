void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
  if (huart->Instance == USART1) {
    if (rxByte == '\n' || rxByte == '\r') {
      rxBuffer[rxIndex] = '\0'; // Null terminate string

      // Parse Command from ESP32 using strstr to ignore garbage data
      if (strstr((char *)rxBuffer, "CMD:ON") != NULL) {
        isAutoMode = 0; // Disable Auto mode
        turnOnRelay();
      } else if (strstr((char *)rxBuffer, "CMD:OFF") != NULL) {
        isAutoMode = 0; // Disable Auto mode
        turnOffRelay();
      } else if (strstr((char *)rxBuffer, "CMD:TOGGLE") != NULL) {
        isAutoMode = 0; // Disable Auto mode
        toggleRelay();
      } else if (strstr((char *)rxBuffer, "CMD:AUTO") != NULL) {
        isAutoMode = 1; // Enable Auto mode
      }

      rxIndex = 0; // Reset buffer index
    } else {
      if (rxIndex < sizeof(rxBuffer) - 1) {
        rxBuffer[rxIndex++] = rxByte;
      }
    }

    // Re-enable interrupt to receive next byte
    HAL_UART_Receive_IT(&huart1, &rxByte, 1);
  }
}

void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart) {
  if (huart->Instance == USART1) {
    // Restart RX if an Overrun, Noise, or Framing error occurs
    HAL_UART_Receive_IT(&huart1, &rxByte, 1);
  }
}
