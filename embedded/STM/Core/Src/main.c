/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick.
   */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  initRelay();
  // 1. แค่ Calibrate ADC ตรงนี้พอ
  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);
  initDustSensor();
  initDHTSensor();

  // Start UART RX Interrupt
  HAL_UART_Receive_IT(&huart1, &rxByte, 1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1) {
    uint32_t currentTick = HAL_GetTick();
    if (currentTick - lastReadTick >= 2000) {
      lastReadTick = currentTick;

      // Read Sensors
      float raw_dust = readDustDensity(&hadc1);
      float raw_temp = 0.0, raw_hum = 0.0;
      uint8_t dht_ok = readDHT22(&raw_temp, &raw_hum);

      // --- 1. OOR (Out Of Range) Check ---
      if (raw_dust < 0.0)
        raw_dust = 0.0;
      else if (raw_dust > 2000.0)
        raw_dust = 2000.0; // Reasonable max limits

      if (dht_ok) {
        if (raw_temp < -40.0 || raw_temp > 80.0)
          dht_ok = 0; // Temp OOR
        if (raw_hum < 0.0 || raw_hum > 100.0)
          dht_ok = 0; // Hum OOR
      }

      // Update Temp/Hum only if data is valid and in range
      if (dht_ok) {
        tempVal = raw_temp;
        humVal = raw_hum;
      }

      // --- 2. Moving Average (Dust 30 seconds) ---
      dust_ma[dust_ma_idx] = raw_dust;
      dust_ma_idx = (dust_ma_idx + 1) % DUST_MA_SIZE;
      if (dust_ma_count < DUST_MA_SIZE)
        dust_ma_count++;

      float sum_dust = 0;
      for (int i = 0; i < dust_ma_count; i++) {
        sum_dust += dust_ma[i];
      }
      dustVal = sum_dust / dust_ma_count;

      // --- 3. Decision Logic (Only applies if in Auto Mode) ---
      if (isAutoMode) {
        if (dustVal > 50.0) {
          turnOnRelay();
        } else if (dustVal < 30.0) {
          turnOffRelay();
        }
      }

      // Prepare Data (Send Mode status too: M:1 for Auto, M:0 for Manual)
      char buffer[64];
      snprintf(buffer, sizeof(buffer), "PM:%.1f,T:%.1f,H:%.1f,R:%d,M:%d\n",
               dustVal, tempVal, humVal, getRelayState(), isAutoMode);

      // Send via UART
      HAL_UART_Transmit(&huart1, (uint8_t *)buffer, strlen(buffer), 100);
    }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

